export function generateRandomSentence({
  difficulty,
  length = 10,
}: {
  difficulty: "easy" | "medium" | "hard"
  length?: number
}) {
  const available_words =
    difficulty === "easy"
      ? words.easy
      : difficulty === "medium"
        ? words.easy.concat(words.medium)
        : words.easy.concat(words.medium).concat(words.hard)
  let sentence: string[] = []

  for (let i = 0; i < length; i++) {
    // Pick a random word
    const r = Math.floor(Math.random() * available_words.length)
    const newWord = available_words[r]

    // Compare the word to the last word, reset `i` if necessary
    const lastWord = sentence.at(-1)
    if (newWord === lastWord) {
      i -= 1
      continue
    }

    sentence.push(available_words[r])
  }

  return sentence.join(" ")
}
